<center><h1>Upload iPod</h1></center>
<div style="margin: 10px;">
An easy way to add all your music is to use the information already on your iPod. Not the actual music but the database of that music. There are two ways to do this.
<p>
<b>1. Upload the database directly.</b>
<p>
If you know where your database is then you can use the upload form bellow. Usually the database is in "iPod_Control/iTunes" and the file name is often something like iTunesDB. You can access this file when your iPod is mounted as a disk drive.<br>
Not supported, ignore this section. Just use the software.
<p>
<b>2. Download our software.</b>
<p>
If you can't find the database then let us do it for you!<br>
<a href="ipod_upload/dist/upload_ipod_db.exe">Windows</a><br>
<a href="ipod_upload/upload_ipod_db.py" target="_blank">Cross-platform</a> - Requires <a href="http://www.python.org/" target="_blank">Python 2.6.6</a> and the <a href="ipod_upload/MultipartPostHandler.py" target="_blank">MultipartPostHandler module</a>. (Download and install Python and put upload_ipod_db.py and MultipartPostHandler.py in the same directory. Then run upload_ipod_db.py with Python).
<p>
Once the database is uploaded it will be queued for processing. During this time you may notice new playlists and songs appear on your user page. You will receive another email when the processing is complete. Until then you may not upload anymore databases.

<p>
Remember, it's safe to upload the same database more than once. Any duplicates that already exist on your user page will be skipped.
